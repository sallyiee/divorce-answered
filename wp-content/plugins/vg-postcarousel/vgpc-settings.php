				<h2><strong>VG PostCarousel - Responsive Post Carousel for Wordpress</strong></h2>
				<p><strong>This plugin will help you to show more Post of Wordpress in beautiful responsive carousel slider. It has options for setting the pagination/sliding speed, enabling/disabling autoplay, showing bullet navigation and more.</strong></p> 

				<p><strong>Also, it is possible to define the number of items to be shown on desktop, tablet + mobile separately and VG PostCarousel will handle that automatically. It supports responsive design and works well on touch devices.</strong></p>

				<p><strong>You can use this plugin to show specify post from a category, you also can choose to show Latest Posts, Order Posts or Featured Posts.</strong></p>			


				<h2><strong>Plugin Features:</strong></h2><br>
				<ol>
					<li>Fully responsive and mobile ready.</li>
					<li>Enable/Disable Touch Swipe by Finger/Mouse.</li>
					<li>Unlimited carousel anywhere by use Shortcode, PHP Code.</li>
					<li>Query post from Latest Posts, Order Posts or Featured Posts.</li>
					<li>Control Number of Post display in a Row and a Column.</li>
					<li>Control Data will display in carousel like: Post Title, Post Image, Post Description, Read more link ...</li>
					<li>You can set background color, item background color, text color, link color, button color.</li>
					<li>You can create new theme, override theme on your wordpress theme.</li>
					<li>And much more features. Please check <a href="http://wordpress.vinagecko.net/p/vg-postcarousel-landing-page/" target="_blank">live demo</a> to see more!</li>
